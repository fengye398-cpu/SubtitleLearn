def show_help(parent=None):
    """显示帮助窗口的便捷函数"""
    if parent is None:
        # 创建临时根窗口
        root = tk.Tk()
        root.withdraw()  # 隐藏根窗口
        help_window = HelpWindow(root)
        help_window.protocol("WM_DELETE_WINDOW", lambda: (help_window.destroy(), root.destroy()))
        root.mainloop()
    else:
        help_window = HelpWindow(parent)

