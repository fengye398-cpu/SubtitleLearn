"""
帮助管理模块
提供软件使用帮助和说明文档
"""

import tkinter as tk
from tkinter import ttk, scrolledtext
import webbrowser
from icon_manager import set_window_icon

class HelpWindow(tk.Toplevel):
    """帮助窗口"""
    
    def __init__(self, parent, on_close_callback=None):
        super().__init__(parent)
        # 改为非模态窗口，允许用户同时操作主窗口和帮助窗口
        # self.transient(parent)  # 移除：这会导致窗口置顶并隐藏最小化按钮
        # self.grab_set()         # 移除：这会阻止用户操作主窗口
        self.parent = parent
        self.on_close_callback = on_close_callback

        self.title("SubtitleLearn - 使用帮助")
        self.geometry("900x700")
        
        # 设置窗口图标
        set_window_icon(self)
        
        # 支持全屏
        self.fullscreen = False
        self.bind("<F11>", self.toggle_fullscreen)
        self.bind("<Escape>", self.exit_fullscreen)
        
        self.create_widgets()

        # 绑定关闭事件
        self.protocol("WM_DELETE_WINDOW", self._on_close)

        # 居中显示
        self.center_window()

    def _on_close(self):
        """窗口关闭处理"""
        if self.on_close_callback:
            self.on_close_callback()
        self.destroy()
    
    def center_window(self):
        """窗口居中显示"""
        self.update_idletasks()
        width = self.winfo_width()
        height = self.winfo_height()
        x = (self.winfo_screenwidth() // 2) - (width // 2)
        y = (self.winfo_screenheight() // 2) - (height // 2)
        self.geometry(f"{width}x{height}+{x}+{y}")
    
    def toggle_fullscreen(self, event=None):
        """切换全屏模式"""
        self.fullscreen = not self.fullscreen
        self.attributes("-fullscreen", self.fullscreen)
        return "break"
    
    def exit_fullscreen(self, event=None):
        """退出全屏模式"""
        self.fullscreen = False
        self.attributes("-fullscreen", False)
        return "break"
    
    def create_widgets(self):
        """创建界面组件"""
        main_frame = ttk.Frame(self)
        main_frame.pack(fill=tk.BOTH, expand=True, padx=10, pady=10)
        
        # 标题栏
        title_frame = ttk.Frame(main_frame)
        title_frame.pack(fill=tk.X, pady=(0, 10))

        title_label = ttk.Label(title_frame, text="SubtitleLearn 使用帮助",
                               font=("", 16, "bold"))
        title_label.pack(side=tk.LEFT)
        
        # 创建笔记本控件（标签页）
        notebook = ttk.Notebook(main_frame)
        notebook.pack(fill=tk.BOTH, expand=True)
        
        # 软件介绍标签页
        intro_frame = ttk.Frame(notebook)
        notebook.add(intro_frame, text="软件介绍")
        self.create_intro_tab(intro_frame)
        
        # 快速入门标签页
        quickstart_frame = ttk.Frame(notebook)
        notebook.add(quickstart_frame, text="快速入门")
        self.create_quickstart_tab(quickstart_frame)
        
        # 搜索与过滤标签页
        search_filter_frame = ttk.Frame(notebook)
        notebook.add(search_filter_frame, text="搜索与过滤")
        self.create_search_filter_tab(search_filter_frame)

        # 功能详解标签页
        features_frame = ttk.Frame(notebook)
        notebook.add(features_frame, text="功能详解")
